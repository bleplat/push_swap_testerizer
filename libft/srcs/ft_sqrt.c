int			ft_sqrt(int nb)
{
	int		test;

	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	test = 3;
	while (test <= nb / 3 && test < 46500)
	{
		if (test * test == nb)
			return (test);
		test++;
	}
	return (0);
}
