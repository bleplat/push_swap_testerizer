#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*cur;
	size_t	i2;

	cur = s1;
	while (*cur != '\0')
		cur++;
	i2 = 0;
	while (i2 < n && s2[i2] != '\0')
	{
		*cur = s2[i2];
		cur++;
		i2++;
	}
	*cur = '\0';
	return (s1);
}
